    private void editActionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionBtnActionPerformed
        // TODO add your handling code here:
        int row = bookUITable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a book to edit.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        new BookFormFrame(Integer.parseInt(bookUITable.getValueAt(row, 0).toString())).setVisible(true);
        dispose();
    }//GEN-LAST:event_editActionBtnActionPerformed

    private void deleteActionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionBtnActionPerformed
        // TODO add your handling code here:
        int row = bookUITable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a book to delete.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this book?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        Response<Void> response = service.delete((int) bookUITable.getValueAt(row, 0));
        if (!response.isSuccess()) {
            JOptionPane.showMessageDialog(this, response.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        ((DefaultTableModel) bookUITable.getModel()).removeRow(row);
        JOptionPane.showMessageDialog(this, "Book deleted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_deleteActionBtnActionPerformed

    private void searchActionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionBtnActionPerformed
        // TODO add your handling code here:
        String query = searchField.getText().trim();

        if (query.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a search query.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        mapBooksToTable(query);
    }//GEN-LAST:event_searchActionBtnActionPerformed

