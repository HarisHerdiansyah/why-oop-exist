    private void mapBooksToTable(String query) {
        Response<List<Book>> response = service.getAll(query);

        if (!response.isSuccess()) {
            JOptionPane.showMessageDialog(this, response.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        List<Book> books = response.getData();
        DefaultTableModel model = (DefaultTableModel) bookUITable.getModel();
        model.setNumRows(0);
        for (Book book : books) {
            model.addRow(new Object[]{
                book.getId(),
                book.getTitle(),
                book.getStartDate(),
                book.getStatus(),
                book.getCategory().getName()
            });
        }
    }

