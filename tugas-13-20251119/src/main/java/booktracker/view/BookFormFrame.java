    private Book validateBook() {
        try {
            String title = titleField.getText().trim();
            ComboBoxItem selectedCategoryItem = (ComboBoxItem) categoryComboBox.getSelectedItem();
            String startDateStr = startDateField.getText().trim();
            String endDateStr = isEdit ? endDateField.getText().trim() : "";
            String currentPageStr = currentPageField.getText().trim();
            String totalPagesStr = totalPagesField.getText().trim();
            ComboBoxItem selectedStatusItem = (ComboBoxItem) statusComboBox.getSelectedItem();

            if (title.isEmpty() || selectedCategoryItem == null || startDateStr.isEmpty() || currentPageStr.isEmpty() || totalPagesStr.isEmpty() || selectedStatusItem == null) {
                throw new IllegalArgumentException("Please fill in all fields.");
            }

            if (isEdit && endDateStr.isEmpty()) {
                throw new IllegalArgumentException("Please fill in all fields.");
            }

            BookCategory category = new BookCategory((int) selectedCategoryItem.getValue(), selectedCategoryItem.toString());
            LocalDate startDate = LocalDate.parse(startDateStr);
            LocalDate endDate = isEdit ? LocalDate.parse(endDateStr) : null;
            int currentPage = Integer.parseInt(currentPageStr);
            int totalPages = Integer.parseInt(totalPagesStr);
            Status selectedStatus = (Status) selectedStatusItem.getValue();

            if (startDate.isAfter(endDate)) {
                throw new IllegalArgumentException("Start date cannot be after end date.");
            }

            return new Book(this.objectId, title, category, startDate, endDate, currentPage, totalPages, selectedStatus);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Current Page and Total Pages must be valid integers.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        return null;
    }

    private void manageAction() {
        try {
            Book book = validateBook();

            if (book == null) {
                return;
            }

            Response<Void> response = isEdit ? bookService.update(book) : bookService.create(book.getTitle(), book.getCategory(), book.getStartDate(), book.getCurrentPage(), book.getTotalPages());
            if (!response.isSuccess()) {
                JOptionPane.showMessageDialog(this, "Failed to " + (isEdit ? "update" : "create") + " book: " + response.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            JOptionPane.showMessageDialog(this, "Book " + (isEdit ? "updated" : "created") + " successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            backAction();
            clearField();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

