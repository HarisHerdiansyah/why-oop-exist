    private void populateCategoryComboBox() {
        Response<List<BookCategory>> response = bookCategoryService.getAll(null);
        if (!response.isSuccess()) {
            JOptionPane.showMessageDialog(this, "Failed to load categories: " + response.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        List<BookCategory> categories = response.getData();
        int index = 0;
        for (int i = 0; i < categories.size(); i++) {
            BookCategory category = categories.get(i);
            ComboBoxItem item = new ComboBoxItem(category.getName(), category.getId());
            categoryComboBox.addItem(item);
            if (isEdit && category.getId() == objectId) {
                index = i;
            }
        }

        if (isEdit) {
            categoryComboBox.setSelectedIndex(index);
        }
    }

    private void populateStatusComboBox() {
        int index = 0;
        Status[] statuses = Status.values();
        for (int i = 0; i < statuses.length; i++) {
            Status status = statuses[i];
            ComboBoxItem item = new ComboBoxItem(status.name().replace("_", " "), status);
            statusComboBox.addItem(item);
            if (isEdit && status == bookService.getById(objectId).getData().getStatus()) {
                index = i;
            }
        }

        if (isEdit) {
            statusComboBox.setSelectedIndex(index);
        }
    }

    private void initiateFieldForUpdate() {
        Response<Book> response = bookService.getById(objectId);
        if (!response.isSuccess()) {
            JOptionPane.showMessageDialog(this, "Failed to load book: " + response.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Book book = response.getData();
        titleField.setText(book.getTitle());
        startDateField.setText(book.getStartDate().toString());
        endDateField.setText(book.getEndDate() != null ? book.getEndDate().toString() : "");
        currentPageField.setText(String.valueOf(book.getCurrentPage()));
        totalPagesField.setText(String.valueOf(book.getTotalPages()));
    }

