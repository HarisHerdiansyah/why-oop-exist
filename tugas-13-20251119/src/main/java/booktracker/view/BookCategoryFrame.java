    private void mapCategoriesToTable(String query) {
        // Method to map categories to the table (to be implemented)
        Response<List<BookCategory>> response = service.getAll(query);

        if (!response.isSuccess()) {
            JOptionPane.showMessageDialog(this, response.getMessage());
            return;
        }

        List<BookCategory> categories = response.getData();
        DefaultTableModel model = (DefaultTableModel) bookCategoryUITable.getModel();
        model.setNumRows(0);
        for (BookCategory category : categories) {
            Object[] rowData = {category.getId(), category.getName()};
            model.addRow(rowData);
        }
    }

