    private void editCatActionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editCatActionBtnActionPerformed
        // TODO add your handling code here:
        int row = bookCategoryUITable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a category to edit.");
            return;
        }

        new BookCategoryFormFrame(Integer.parseInt(bookCategoryUITable.getValueAt(row, 0).toString())).setVisible(true);
        dispose();
    }//GEN-LAST:event_editCatActionBtnActionPerformed

    private void deleteCatActionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCatActionBtnActionPerformed
        // TODO add your handling code here:
        int row = bookCategoryUITable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a category to delete.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete the selected category?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        Response<Void> response = service.delete((Integer) bookCategoryUITable.getValueAt(row, 0));
        if (!response.isSuccess()) {
            JOptionPane.showMessageDialog(this, response.getMessage());
            return;
        }

        ((DefaultTableModel) bookCategoryUITable.getModel()).removeRow(row);
        JOptionPane.showMessageDialog(this, response.getMessage());
    }//GEN-LAST:event_deleteCatActionBtnActionPerformed

    private void searchCatActionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchCatActionBtnActionPerformed
        // TODO add your handling code here:
        String query = searchCatField.getText().trim();

        if (query.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a search query.");
            return;
        }

        this.mapCategoriesToTable(query);
    }//GEN-LAST:event_searchCatActionBtnActionPerformed

